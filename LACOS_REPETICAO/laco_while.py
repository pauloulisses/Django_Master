# Aqui onde inicia-se a contagem
numero = 0

# Enquanto numero for menor que 5
while numero < 5:
    # Se numero for igual a 2, incremente e continue
    if numero == 2:
        numero += 1
        continue
    print(numero)
    # Aqui está dizendo que será contado de 1 em 1 sequenciado
    numero += 1
